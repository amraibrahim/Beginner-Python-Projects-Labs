# Pattern 1A (ii) - Using a for loop
for i in range(1, 12):
    print(2 * i)

# Pattern 1B (ii) - Using a while loop
n = 1
while n <= 11:
    print(n * n)
    n += 1

# Pattern 1C (ii) - Using a for loop
for i in range(1, 12):
    print(3 * i - 1)

# Pattern 1D (ii) - Using a while loop
n = 1
while n <= 11:
    print(2 ** n)
    n += 1

# Pattern 1E (ii) - Using a for loop
for i in range(1, 12):
    print((i * (i + 1)) // 2)

# Pattern 1F (ii) - Using a while loop
n = 1
while n <= 11:
    print((n * (n + 1)) // 2)
    n += 1

# Pattern 1G (ii) - Using a for loop
for i in range(1, 12):
    print(4 * i - 3)

# Pattern 1H (ii) - Using a while loop
n = 1
while n <= 11:
    print(5 * n - 4)
    n += 1
